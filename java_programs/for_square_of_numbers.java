//print square of numbers from 1 to 10

public class for_square_of_numbers {
    public static void main(String[] args) {
        System.out.println("\nsquare of numbers from 1 to 10");
        for(int i=1;i<=10;i=i+1)
        {
            int b = i * i;
            System.out.println("square of "+i + " is " +b);
        }
    }
}
